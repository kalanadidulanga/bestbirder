const allReviews = async () => {
    setIsLoading(true);
    setError(null);

    try {
        const response = await fetch('http://localhost/bestbirdersl.com/server/all-reviews.php');
        if (!response.ok) {
            throw new Error(`HTTP error! status: ${response.status}`);
        }
        const data = await response.json();
        setData(data);
    } catch (error) {
        setError(error);
    } finally {
        setIsLoading(false);
    }
};